# Tipos de dados 

# Booleano - bool
# Ao fazer um questionamento em um programa só existem duas respostas possíveis:
# sim (True) ou não (false).
# Esistem vários operadores para "questionar". Dentro eles o ==, que é um operador lógico que
# questiona se um valor é igual a outro.

print('Esse valor é: ', 2 == 2)
print('Esse valor é: ', 2 == 3)

# Condicional ternária
print('Esse valor é:', 'Verdadeiro' if 2 == 3 else 'Falso')
print('Esse valor é:', 'Verdadeiro' if 2 == 2 else 'Falso')