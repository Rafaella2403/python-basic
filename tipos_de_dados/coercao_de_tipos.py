# Coerção de tipos

# Coerção de tipos também chamado de conversão de tipo, type conversion, typecasting, coercion
# é o ato de converter um tipo em outro. Tipos imutáveis e primitivos:
# str, int, float, bool
print(int('1') + 1)
print(float('1') + 1)
print('1' + str(1))
print(bool(''))
print(bool(' '))
print(bool(1))
print(bool(0))